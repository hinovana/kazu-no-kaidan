import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

import { PROTOTYPE_LEXICON } from "../infrastructure/language/prototype-language-data-provider.ts";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const GENERATOR_DIR = resolve(SCRIPT_DIR, "..");

export const DEFAULT_GENERATOR_VOCABULARY_PROJECTION_PATHS = Object.freeze({
  manifestPath: resolve(GENERATOR_DIR, "data/generated/vocabulary-db.manifest.json"),
  lowerPath: resolve(GENERATOR_DIR, "data/generated/vocabulary-lower-elementary.json"),
  upperPath: resolve(GENERATOR_DIR, "data/generated/vocabulary-upper-elementary.json"),
  outputPath: resolve(GENERATOR_DIR, "src/generated/prototype-vocabulary-evidence.ts"),
});

async function readJson(path) {
  return JSON.parse(await readFile(path, "utf8"));
}

function matchesEntry(record, entry) {
  return record.headword === entry.reading
    && typeof record.notation === "string"
    && record.notation.includes(entry.surface);
}

function stableProjectionModule(databaseRelease, projection) {
  const projectionLines = Object.entries(projection).map(([id, evidence]) =>
    `  ${JSON.stringify(id)}: Object.freeze(${JSON.stringify(evidence)}),`);
  return [
    "// Generated by scripts/build-generator-vocabulary-projection.mjs.",
    "// Do not edit by hand. This is a small browser-safe projection, not the full vocabulary DB.",
    `export const VOCABULARY_CANDIDATE_DATABASE_RELEASE = ${JSON.stringify(databaseRelease)};`,
    "",
    "export const PROTOTYPE_VOCABULARY_EVIDENCE = Object.freeze({",
    ...projectionLines,
    "});",
    "",
  ].join("\n");
}

export async function buildGeneratorVocabularyProjection({
  manifestPath = DEFAULT_GENERATOR_VOCABULARY_PROJECTION_PATHS.manifestPath,
  lowerPath = DEFAULT_GENERATOR_VOCABULARY_PROJECTION_PATHS.lowerPath,
  upperPath = DEFAULT_GENERATOR_VOCABULARY_PROJECTION_PATHS.upperPath,
  outputPath = DEFAULT_GENERATOR_VOCABULARY_PROJECTION_PATHS.outputPath,
  prototypeLexicon = PROTOTYPE_LEXICON,
} = {}) {
  const [manifest, lower, upper] = await Promise.all([
    readJson(manifestPath),
    readJson(lowerPath),
    readJson(upperPath),
  ]);
  const projection = {};

  for (const [prototypeLexemeId, entry] of Object.entries(prototypeLexicon)) {
    const lowerMatches = lower.records.filter((record) => matchesEntry(record, entry));
    const upperMatches = upper.records.filter((record) => matchesEntry(record, entry));
    if (lowerMatches.length !== 1) {
      const upperOnly = lowerMatches.length === 0 && upperMatches.length > 0;
      throw new Error(
        `${prototypeLexemeId}: expected one lower-elementary vocabulary match for ${entry.surface}/${entry.reading}; ` +
        `lower=${lowerMatches.length}, upper=${upperMatches.length}, upper_only=${upperOnly}`,
      );
    }
    const source = lowerMatches[0];
    projection[prototypeLexemeId] = {
      source_lexeme_id: source.lexeme_id,
      surface: entry.surface,
      reading: entry.reading,
      grade_band: source.grade_band,
      source_allocation_code: source.source_allocation_code,
      source_id: source.source_id,
      evidence_status: source.usage_status,
      generation_eligible: source.active_for_generation,
      upper_elementary_homograph_ids: upperMatches.map((record) => record.lexeme_id),
    };
  }

  const content = stableProjectionModule(manifest.database_release, projection);
  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, content);
  return {
    database_release: manifest.database_release,
    record_count: Object.keys(projection).length,
    projection,
  };
}

function parseArgs(argv) {
  const values = {};
  for (let index = 0; index < argv.length; index += 2) {
    const flag = argv[index];
    const value = argv[index + 1];
    if (!flag?.startsWith("--") || value === undefined) {
      throw new Error(`Expected --name value arguments, got: ${argv.join(" ")}`);
    }
    values[flag.slice(2)] = resolve(value);
  }
  return {
    manifestPath: values.manifest,
    lowerPath: values.lower,
    upperPath: values.upper,
    outputPath: values.output,
  };
}

if (process.argv[1] && import.meta.url === pathToFileURL(resolve(process.argv[1])).href) {
  buildGeneratorVocabularyProjection(parseArgs(process.argv.slice(2)))
    .then((summary) => {
      console.log(
        `Built generator vocabulary projection: ${summary.record_count} records from ${summary.database_release}`,
      );
    })
    .catch((error) => {
      console.error(error.message);
      process.exitCode = 1;
    });
}
